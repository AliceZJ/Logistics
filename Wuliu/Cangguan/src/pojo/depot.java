package pojo;

public class depot {
	private Integer d_id_hyj;
	private String d_name_hyj;
	private Integer d_area_hyj;
	private String d_type_hyj;
	private String d_addr_hyj;
	
	public depot() {
		super();
	}
	public depot(Integer d_id_hyj, String d_name_hyj, Integer d_area_hyj,
			String d_type_hyj, String d_addr_hyj) {
		super();
		this.d_id_hyj = d_id_hyj;
		this.d_name_hyj = d_name_hyj;
		this.d_area_hyj = d_area_hyj;
		this.d_type_hyj = d_type_hyj;
		this.d_addr_hyj = d_addr_hyj;
	}
	public Integer getD_id_hyj() {
		return d_id_hyj;
	}
	public void setD_id_hyj(Integer d_id_hyj) {
		this.d_id_hyj = d_id_hyj;
	}
	public String getD_name_hyj() {
		return d_name_hyj;
	}
	public void setD_name_hyj(String d_name_hyj) {
		this.d_name_hyj = d_name_hyj;
	}
	public Integer getD_area_hyj() {
		return d_area_hyj;
	}
	public void setD_area_hyj(Integer d_area_hyj) {
		this.d_area_hyj = d_area_hyj;
	}
	public String getD_type_hyj() {
		return d_type_hyj;
	}
	public void setD_type_hyj(String d_type_hyj) {
		this.d_type_hyj = d_type_hyj;
	}
	public String getD_addr_hyj() {
		return d_addr_hyj;
	}
	public void setD_addr_hyj(String d_addr_hyj) {
		this.d_addr_hyj = d_addr_hyj;
	}
	
	

}

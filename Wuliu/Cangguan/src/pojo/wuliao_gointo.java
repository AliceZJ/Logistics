package pojo;

public class wuliao_gointo {
private wuliao wuliao;
private gointo gointo;
public wuliao_gointo() {
	super();
}
public wuliao_gointo(pojo.wuliao wuliao, pojo.gointo gointo) {
	super();
	this.wuliao = wuliao;
	this.gointo = gointo;
}
public wuliao getWuliao() {
	return wuliao;
}
public void setWuliao(wuliao wuliao) {
	this.wuliao = wuliao;
}
public gointo getGointo() {
	return gointo;
}
public void setGointo(gointo gointo) {
	this.gointo = gointo;
}

}
